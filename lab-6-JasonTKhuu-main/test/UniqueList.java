
public class UniqueList extends IntegerList {
	
	public UniqueList() { 
		super();
	}
	
	public UniqueList(int capacity) {
		super(capacity);
	}
	
	@Override
	public void add(int integer) {
		super.add(integer);
	}
	 
	@Override
	public void insert(int index, int integer) {
		super.insert(index, integer);
	}
}
